import Foundation
import UIKit

extension UIColor {
    class func dondeBeigeColor() -> UIColor {
        return UIColor(red: 1, green: 232/255, blue: 202/255, alpha: 1.0)
    }
    
    class func dondeGreenColor() -> UIColor {
        return UIColor(red: 19/255, green: 152/255, blue: 138/255, alpha: 1.0)
    }
   
    class func dondeBlueColor() -> UIColor {
        return UIColor(red: 25/255, green: 118/255, blue: 210/255, alpha: 1.0)
    }
    
    class func dondeGreenLightColor() -> UIColor {
        return UIColor(red: 19/255, green: 152/255, blue: 138/255, alpha: 0.2)
    }
    
    class func dondeRedColor() -> UIColor {
        return UIColor(red: 230/255, green: 74/255, blue: 25/255, alpha: 1.0)
    }
    
    class func dondeRedLightColor() -> UIColor {
        return UIColor(red: 238/255, green: 103/255, blue: 100/255, alpha: 0.2)
    }
    
    class func dondeBrownColor() -> UIColor {
        return UIColor(red: 55/255, green: 31/255, blue: 31/255, alpha: 1.0)
    }
    
    class func dondeYellowColor() -> UIColor {
        return UIColor(red: 249/255, green: 168/255, blue: 37/255, alpha: 1.0)
    }
    
    class func dondeWhiteColor() -> UIColor {
        return UIColor(red: 236/255, green: 239/255, blue: 241/255, alpha: 1.0)
    }
}
